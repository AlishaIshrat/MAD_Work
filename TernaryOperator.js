// simple if using ternary operator
if(4<5)
    {
        console.log("4 is less than 5");
    
    }
    else{
        console.log(" default error");
    }
    // Using ternary instead of simple if statement
    (4<5) ? console.log('4 is less than 5') : console.log('default error');
    //logical and
    (4<5) && console.log('4 is less than 5');